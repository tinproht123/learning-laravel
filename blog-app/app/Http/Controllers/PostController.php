    /**
     * Store a newly created resource in storage.
     */
    public function store(PostFormRequest $request)
    {
        $request->validated();

        Post::create([
            'title' => $request->title,
            'body' => $request->body,
            'summary' => $request->summary,
            'user_id' => 1,
            'slug' => Str::slug($request->title)
        ]);

        return redirect(route('post.index'));
    }

